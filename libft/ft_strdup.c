#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ptr;
	int		l;
	int		i;

	i = 0;
	l = ft_strlen(s1);
	ptr = malloc((l + 1) * sizeof(char));
	if (!ptr)
		return (0);
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
