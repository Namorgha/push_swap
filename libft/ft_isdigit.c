#include "libft.h"

int	ft_isdigit(char *c)
{
	int	i;

	i = 0;
	while ((c[i] == '+') || (c[i] == '-' ) || (c[i] == ' '))
		i++;
	if (c[i] == 0)
		return (0);
	while (c[i])
	{
		if (!(c[i] >= '0' && c[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
